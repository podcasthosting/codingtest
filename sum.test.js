const sum = require('./sum');

test('get sums', () => {
    let data = {
        2024: {
            1: 10,
            2: 25,
            3: 5,
            4: 30
        },
        2025: {
            1: 20,
            2: 15,
            3: 15,
            4: 40
        },
        2026: {
            1: 5,
            2: 5,
            3: 10,
            4: 60
        }
    }
    expect(sum(data)).toBe({
        2024: 70,
        2025: 90,
        2026: 80
    });
});